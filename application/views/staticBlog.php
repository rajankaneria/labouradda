<p dir="ltr"><strong>Another important thing that we came across was that there is no functional respite for the consumer from the government. Lack of any proper communication channels between consumers and labourers is a major setback to the mandi dynamics. Dearth of any bargain (in terms of money or number of working hours) guidelines is another major downside from a consumers point of view. &nbsp;&nbsp;</strong></p>



<p dir="ltr"><strong>It is unfortunate that this highly potent sector is significantly &lsquo;unorganized&rsquo;, marred with such momentous issues and is scarcely surviving with a 2% annual growth. Imagine the growth that our country can achieve if we are able to offer guaranteed daily employment to this large population! </strong></p>


<p  style="font-size: 18px;font-weight: normal;color: #505050; margin-top: 30px;">Inception of an Innovation</p>
<p dir="ltr"><strong>


<p><b style="color: black;">Labouradda </b> has shaped up into a unique digital platform that enables <b style="color: black;">direct</b> <b style="color: black;">communication</b> between these daily wage labourers and employers, <b style="color: black;">round the clock</b>, i.e. free of the limitations of time and place. Also, we have devised <b style="color: black;">clear cut wage guidelines</b> keeping the needs of consumer and labours in mind.</p>


<p dir="ltr"><strong>Apart from providing a sense of digitized symmetry to the Labour addas, we segmented the labourers into categories including- skilled, semi-skilled and unskilled and cataloged their services for the customers to better access them. &nbsp;&nbsp;</strong></p>
