<p dir="ltr">The inspiration of devising a digital crossroad where demand (consumer) makes direct communication with the supply (manpower) came into being as an urge to find a solution to help an undervalued and humongous section of our society. One morning at an ordinary &lsquo;labour mandi&rsquo; is what it took to motivate us to hustle for a platform where the dynamics of labourers and customers is simplified and put into order.</p>



<p dir="ltr" style="margin-top: 30px;"><strong style="font-size: 18px;font-weight: normal;color: #505050;">Understanding the Dynamics</strong></p>


<div class="row">
	


<div class="col s12 m8">
	
	<p dir="ltr" style="margin-top: 0px"><strong>After an extensive field work for over a year, spanning across different cities we got well versed with how the early morning labour-mandis operate and how a daily wage labourer- who migrates to cities looking for work- is entirely dependent on getting &lsquo;picked-up&rsquo; by a prospective customer. &nbsp;</strong></p>


	<p dir="ltr"><strong>The rates are negotiated which, might or might not benefit the labourers or the consumers. If the labourers make the cut, they are hired to work for approximately eight hours per day. This vicious cycle is repeated every day and is laced with unorganised uncertainty. </strong></p>


	<p dir="ltr"><strong>According to our in-depth survey, these humble daily wagers are the biggest resources that India has and directly contribute to 50% of our country&rsquo;s GDP. </strong></p>



	<p dir="ltr"><strong>The prospective employers who come to the mandis looking for man-power also have to abide by the strict mandi schedule and the labour shift duration. In case the consumer fails to arrive at the mandi on time, there is no way they can track the labourers. </strong></p>
	
</div>



